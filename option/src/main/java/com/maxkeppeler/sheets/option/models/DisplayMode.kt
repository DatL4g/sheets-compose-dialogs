package com.maxkeppeler.sheets.option.models

/**
 *  The display modes that can be used.
 */
enum class DisplayMode {

    /**
     * The options are displayed in a row horizontally and can be scrolled.
     */
    GRID_HORIZONTAL,

    /**
     * The options are displayed in a grid with the specified amount of columns.
     */
    GRID_VERTICAL,

    /**
     * The options are displayed vertically in a list.
     */
    LIST
}